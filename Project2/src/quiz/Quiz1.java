package quiz;

public class Quiz1 {

	public static void main(String[] args) {
		
		// int형 변수 x 선언
		int x;
		// x에 1을 저장
		x = 1;
		// x의 값을 5호 변경
		x = 5;
		// x를 출력
		System.out.println(x);
		
		// int형 변수 y 선언하고 바로 3을 대입
		int y = 3;
		// y의 값을 6으로 변경
		y = 6;
		// y를 출력
		System.out.println(y);

	}

}
